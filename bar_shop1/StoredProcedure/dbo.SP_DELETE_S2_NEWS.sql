CREATE PROCEDURE [dbo].[SP_DELETE_S2_NEWS]
    @SEQS AS VARCHAR(4000)
AS
BEGIN

    DECLARE @T_SEQS TABLE
    (
		SEQ int
	)
	
	INSERT INTO @T_SEQS (SEQ)
    SELECT CAST(value AS INT) FROM dbo.[ufn_SplitTable] (@SEQS, '|')



    DELETE  S2_NEWS
    WHERE   SEQ IN (SELECT SEQ FROM @T_SEQS)

END

GO
