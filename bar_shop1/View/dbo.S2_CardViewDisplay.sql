CREATE  VIEW [dbo].[S2_CardViewDisplay]
AS
SELECT  Card_Seq,Card_Code, Card_Group as company_seq, DISPLAY_YES_OR_NO as isDisplay,JUMUN_YES_OR_NO as isJumun
FROM      Card  where card_cate IN ( 'I1', 'SN','BM' ) and CARD_GROUP in ('2','1801','237')
UNION
SELECT   a.Card_Seq,Card_Code,b.Company_Seq, IsDisplay ,isJumun
FROM      S2_Card a JOIN S2_CardSalesSite b ON a.Card_Seq = b.Card_Seq where A.card_div in ('A01','C03')
